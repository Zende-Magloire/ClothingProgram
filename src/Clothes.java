public abstract class Clothes
{
    private String E_Design;
    private double Size;
    private double Price;


    public String getDesign()
    {
        return E_Design;
    }

    public void setDesign(String d)
    {
        E_Design = d;
    }

    public double getSize()
    {
        return Size;
    }

    public double getPrice()
    {
        return Price;
    }

    public void setPrice(double p)
    {
        Price = p;
    }

}
